export function getTriggerIntentCorpus() {
  return TRIGGER_INTENT_CORPUS.map((entry) => ({
    ...entry,
    prerequisites: [...entry.prerequisites],
    requiredSafety: [...entry.requiredSafety],
  }));
}

export function validateTriggerIntentCorpus(corpus = TRIGGER_INTENT_CORPUS) {
  const issues = [];
  const ids = new Set();

  for (const [index, entry] of corpus.entries()) {
    const prefix = entry?.id ?? `entry-${index}`;
    for (const field of ["id", "phrase", "locale", "intent", "phase", "command", "skill", "nextQuestionIncludes"]) {
      if (typeof entry?.[field] !== "string" || entry[field].trim() === "") {
        issues.push({ id: prefix, code: "missing-field", message: `Missing ${field}` });
      }
    }
    if (ids.has(entry.id)) {
      issues.push({ id: entry.id, code: "duplicate-id", message: `Duplicate corpus id ${entry.id}` });
    }
    ids.add(entry.id);
    if (typeof entry.confidenceFloor !== "number" || entry.confidenceFloor < 0.8 || entry.confidenceFloor > 1) {
      issues.push({ id: prefix, code: "bad-confidence", message: "confidenceFloor must be between 0.8 and 1" });
    }
    if (!Array.isArray(entry.prerequisites) || !Array.isArray(entry.requiredSafety)) {
      issues.push({ id: prefix, code: "bad-arrays", message: "prerequisites and requiredSafety must be arrays" });
    }
  }

  return { pass: issues.length === 0, issues };
}
